def load_sample_packages():
    """Load sample packages from the existing report."""
    report_path = Path("./packster-out/report.json")
    
    if not report_path.exists():
        print(f"âŒ Report file not found: {report_path}")
        print("ğŸ’¡ Run 'packster generate' first to create a report")
        return None
    
    try:
        with open(report_path, 'r') as f:
            report_data = json.load(f)
        
        packages = report_data.get("mapping_results", [])
        if not packages:
            print("âŒ No packages found in report")
            return None
        
        print(f"âœ… Loaded {len(packages)} packages from report")
        return packages
        
    except Exception as e:
        print(f"âŒ Failed to load report: {e}")
        return None


